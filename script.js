function setIconCat(cat, el){
    icCurrentCat = cat;
    document.querySelectorAll('.ic-cat').forEach(e=>e.classList.remove('active'));
    el.classList.add('active');
    filterIcons();
}
function filterIcons(){
    const q = (document.getElementById('ic-search').value||'').toLowerCase();
    const db = icType==='fa' ? FA_ICONS : EMOJI_ICONS;
    if(q){
        const all = Object.values(db).flat();
        const filtered = icType==='fa'
            ? all.filter(i=>i.toLowerCase().includes(q.replace(/\s+/g,'-')))
            : all.filter(i=>i.includes(q));
        renderIconGrid(filtered);
    } else {
        renderIconGrid(db[icCurrentCat] || Object.values(db).flat());
    }
}
function renderIconGrid(icons){
    const grid = document.getElementById('ic-grid');
    if(!grid) return;
    grid.innerHTML = icons.map(ic=>{
        const isSel = ic===selectedIconTemp.val;
        if(icType==='fa'){
            return `<button class="ic-btn${isSel?' sel':''}" onclick="pickIcon('fa','${ic}')" title="${ic}"><i class="${ic}"></i></button>`;
        } else {
            return `<button class="ic-btn${isSel?' sel':''}" onclick="pickIcon('emoji','${ic}')">${ic}</button>`;
        }
    }).join('');
}
function pickIcon(type, val){
    selectedIconTemp = {type, val};
    document.querySelectorAll('.ic-btn').forEach(el=>{
        const ic = el.querySelector('i');
        const sel = ic ? ic.className===val : el.textContent.trim()===val;
        el.classList.toggle('sel', sel);
    });
}
function applyIcon(){
    if(currentPickerLinkId){
        const l = links.find(x=>x.id===currentPickerLinkId);
        if(l){l.iconType=selectedIconTemp.type; l.icon=selectedIconTemp.val;}
        renderLinks();
        debouncedUpdate();
    }
    closeIconModal();
}

